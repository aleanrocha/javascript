/*
  Temos algumas maneiras de atribuir um valor a uma
  variável as mais utilizadas são:
  =+
  -=
  *=
  /=
  Basicamente é uma forma resumida da operação (X = X + Y)
  em loops também é comum utilizar os operadores (++) ou (--)
*/

let x = 1
let y = 2

// Adição 

console.log(x = x + y)
console.log(x+=y)
console.log("----------")

// Subtração

console.log(x = x - 1)
console.log(x-=y)
console.log("----------")


// Multiplicação

console.log(x = x * y)
console.log(x*=y)
console.log("----------")

// Divisão

console.log(x = x / y)
console.log(x/=y)

